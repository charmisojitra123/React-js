// Area of Reactangle
{
    let length = 40;
    let width = 20; 
    let area = length * width;
    console.log(`Area of Reactangle is :- ${area}`);
}

// Area of Circle
{
    const pie = 3.14;
    let radius = 15;
    let area = pie * radius * radius;
    console.log(`Area of Circle is :- ${area}`);
}

// Area of Square
{
    let side = 12;
    let area = side * side;
    console.log(`Area of Square is :- ${area}`);
}

// Area of Triangle
{
    let base = 30;
    let height = 10;
    let area = base * height / 2;
    console.log(`Area of Triangle is :- ${area}`);
}